    class Solution {
        public void solveSudoku(char[][] board) {
            recursion(board, 0, 0);
        }

        private boolean recursion(char[][] board, int row, int col) {

            //æœ€ç»ˆç»“æŸ:ã€æ³¨æ„ã€‘è¿™é‡Œä¸èƒ½æ˜¯row==8 && col == 8ï¼Œè¿™æ ·æœ€åä¸€ä¸ªå…ƒç´ è¿˜æ²¡æœ‰è¢«éå†åˆ°
            if (row == board.length) {
                return true;//é€šè¿‡è¿”å›trueå¿«é€Ÿç»“æŸ
            }

            //è¾¹ç•Œæ¡ä»¶ï¼šåˆ°è¿™ä¸€è¡Œæœ«å°¾ï¼Œè½¬ç§»åˆ°ä¸‹ä¸€è¡Œèµ·å§‹
            if (col == board[0].length) {
                return recursion(board, row + 1, 0);
            }
            //æ’é™¤å½“å‰èŠ‚ç‚¹æ˜¯æœ‰æ•°å­—çš„æƒ…å†µ
            if (board[row][col] != '.') {
                return recursion(board, row, col + 1);
            }
            //åšé€‰æ‹©ï¼šå¯¹äºboard[row][col]å°è¯•ä¸åŒçš„æ•°å­—
            for (char i = '1'; i <= '9'; i++) {
                if (!isValid(board, row, col, i)) {
                    continue;
                }
                board[row][col] = i;
                if (recursion(board, row, col + 1)) {
                    return true;
                }
                board[row][col] = '.';
            }
            return false;
        }

        private boolean isValid(char[][] board, int row, int col, char n) {
            for (char i = 0; i < 9; i++) {
                if (board[i][col] == n) {
                    return false;
                }
                if (board[row][i] == n) {
                    return false;
                }
                if (board[(row / 3) * 3 + i / 3][(col / 3) * 3 + i % 3] == n) {
                    return false;
                }
            }
            return true;
        }


    }
